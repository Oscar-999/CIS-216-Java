package edu.jjc.week8.homework08;

public class Car {
    private String make;
    private int price;
    //Constructor
    public Car(String make, int price) {
        this.make = make;
        this.price = price;
    }
    //getters
    public String getMake() {
        return make;
    }
    public int getPrice() {
        return price;
    }

}
